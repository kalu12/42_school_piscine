#include <unistd.h>

void	ft_is_negative(int n)
{
	char	p ;
	char	s ;

	p = 'P';
	s = 'N';
	if (n < 0)
	{
		write(1, &s, 1);
	}
	else
	{
		write(1, &p, 1);
	}		
}
