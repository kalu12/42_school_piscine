int	ft_str_is_alpha(char *str)
{
	int	x;
	int	ret;

	x = 0;
	ret = 1;
	while (str[x] != '\0')
	{
		if ((str[x] <= 96 || str[x] >= 123) && (str[x] <= 64 || str[x] >= 91))
		{
			ret = 0;
			break ;
		}
	x++;
	}
	return (ret);
}
