void	ft_ultimate_div_mod(int *a, int *b)
{
	int	a1;
	int	b1;

	a1 = *a;
	b1 = *b;
	if (b1 != 0)
	{
		*a = a1 / b1;
		*b = a1 % b1;
	}
}
